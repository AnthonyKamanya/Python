## Lists

# first_element
# takes one list as an arg
# returns the first element

# second_element
# takes one list as an arg
# returns the second element

# last_element
# takes one list as an arg
# returns the last element

# first_two_elements
# takes one list as an arg
# returns the first two elements

# first_three_elements
# takes one list as an arg
# returns the first three elements

# total
# takes one list of numbers as an arg
# adds all the numbers together
# returns the total

# lowest_number
# takes one list of numbers as an arg
# returns the lowest value

# highest_number
# takes one list of numbers as an arg
# returns the highest value

# the_beatles
# takes no args
# returns ['john', 'paul', 'george', 'ringo']

# i_joined_the_beatles
# takes one string as an arg
# when that string is 'freda'
# returns ['john', 'paul', 'george', 'ringo', 'freda']

# we_joined_the_beatles
# takes one list as an arg
# when the list is ['sam', 'alex']
# returns ['john', 'paul', 'george', 'ringo', 'sam', 'alex']

# remove_nones_from_list
# takes one list as an arg
# returns the list, minus any none values

# double_list
# takes one list as an arg
# when that list is [1,2,3]
# returns [1,2,3,1,2,3]

# unique_elements
# takes one list as an arg
# when that list is [1,1,2,2,3,3]
# returns [1,2,3]

# add_to_list
# takes one list and one string as args
# adds the string to the list
# returns the new list

## Dictionaries

# new_band_member
# takes a dictionary of one key-value pair as an arg
# merges it wth the existing dictionary of band members
# returns the new dictionary
# the existing band members are: {'vocalist' => 'miss piggy', 'lead_guitar' => 'scooter'
# if the arg is {'drummer' => 'kermit'}
# returns {'vocalist' => 'miss piggy', 'lead_guitar' => 'scooter', 'drummer' => 'kermit'}

# all_values
# takes one dictionary as an arg
# returns the values

# all_keys
# takes one dictionary as an arg
# returns the keys

# remove_nones_from_dictionary
# takes one dictionary an an arg
# removes key-value pairs where the value is None
# returns the remaining key-value pairs as a dictionary

# key_value_swap
# takes one dictionary as an arg
# swaps the keys with the values
# returns the new dictionary

